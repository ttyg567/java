package whilecontrol;

public class P92 {

	public static void main(String[] args) {
		
		// ctrl + shift + f :줄 맞춤
		
		System.out.println("Start .... ");

		for (int i = 0; i < 10; i++) {   // 0 ~ 9 : 10번 실행
			System.out.println(i);

		}
		
		// 1 부터 10
		/*
		 * for (int i = 1; i <= 10; i++) { 
		 *  System.out.println(i);
		 * 
		 * }
		 */
		System.out.println("End .... ");

	}

}
